// Export utility functions for lesson management
export function getLessonByNumber(lessonNumber) {
  return fundamentalBasicLessons[`lesson_${lessonNumber}`];
}

export function getAllLessonObjectives() {
  const objectives = [];
  for (let i = 1; i <= 8; i++) {
    const lesson = fundamentalBasicLessons[`lesson_${i}`];
    if (lesson && lesson.learning_objectives) {
      objectives.push({
        lesson: i,
        title: lesson.title,
        objectives: lesson.learning_objectives
      });
    }
  }
  return objectives;
}

export function getVocabularyByLesson(lessonNumber) {
  const lesson = fundamentalBasicLessons[`lesson_${lessonNumber}`];
  return lesson ? lesson.vocabulary_focus : [];
}

export function getCulturalContentByLesson(lessonNumber) {
  const lesson = fundamentalBasicLessons[`lesson_${lessonNumber}`];
  return lesson ? lesson.cultural_integration : null;
}

