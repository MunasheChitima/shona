// Export utility functions for audio content management
export function getAudioContentByModule(moduleName) {
  return audioContentFramework[`${moduleName}_audio`];
}

export function getPronunciationScripts(category) {
  // Return pronunciation scripts for specific vocabulary categories
  const scripts = [];
  Object.values(audioContentFramework).forEach(module => {
    if (module.recording_scripts) {
      scripts.push(...module.recording_scripts.filter(script => 
        script.content.toLowerCase().includes(category.toLowerCase())
      ));
    }
  });
  return scripts;
}

export function getCulturalAudioGuidelines() {
  return audioContentFramework.traditional_cultural_audio.ceremonial_vocabulary_audio.cultural_sensitivity_protocol;
}

export function getRecordingSpecifications() {
  return audioContentFramework.pronunciation_guide_system.recording_specifications;
}

export function getProductionWorkflow() {
  return audioContentFramework.production_workflow;
}

// Audio file naming and organization system
export function generateAudioFileName(moduleType, category, itemId, audioType) {
  // Generate systematic audio file names
  const timestamp = new Date().toISOString().slice(0, 10);
  return `${moduleType}_${category}_${itemId}_${audioType}_${timestamp}.wav`;
}

