// Helper functions
export function getContemporaryVocabularyByCategory(category) {
  const allWords = []
  
  Object.values(contemporaryModernVocabulary).forEach(mainCategory => {
    Object.values(mainCategory).forEach(subCategory => {
      if (Array.isArray(subCategory)) {
        allWords.push(...subCategory.filter(word => word.category === category))
      }
    })
  })
  
  return allWords
}

export function getContemporaryVocabularyBySubcategory(subcategory) {
  const allWords = []
  
  Object.values(contemporaryModernVocabulary).forEach(mainCategory => {
    Object.values(mainCategory).forEach(subCategory => {
      if (Array.isArray(subCategory)) {
        allWords.push(...subCategory.filter(word => 
          word.subcategories.includes(subcategory)
        ))
      }
    })
  })
  
  return allWords
}

export function searchContemporaryVocabulary(searchTerm) {
  const allWords = []
  const term = searchTerm.toLowerCase()
  
  Object.values(contemporaryModernVocabulary).forEach(mainCategory => {
    Object.values(mainCategory).forEach(subCategory => {
      if (Array.isArray(subCategory)) {
        allWords.push(...subCategory.filter(word => 
          word.shona.toLowerCase().includes(term) ||
          word.english.toLowerCase().includes(term) ||
          word.subcategories.some(sub => sub.toLowerCase().includes(term))
        ))
      }
    })
  })
  
  return allWords
}

